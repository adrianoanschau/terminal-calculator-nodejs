module.exports = {
    sum: function(value1, value2){
        return parseFloat(value1) + parseFloat(value2);
    },
    subtraction: function(value1, value2){
        return parseFloat(value1) - parseFloat(value2);
    },
    multiplication: function(value1, value2){
        return parseFloat(value1) * parseFloat(value2);
    },
    division: function(value1, value2){
        return parseFloat(value1) / parseFloat(value2);
    },
};